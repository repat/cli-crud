    public static function render(?string $text): string
    {
        if (empty($text)) {
            $text = 'CLI CRUD';
        }

        $text = strtoupper($text);
        $lines = ['', '', '', '', ''];

        for ($i = 0; $i < strlen($text); $i++) {
            $char = $text[$i];
            $charLines = self::$font[$char] ?? self::$font[' '];

            for ($line = 0; $line < 5; $line++) {
                $lines[$line] .= $charLines[$line];
            }
        }

        return implode("\n", $lines);
    }
